package Q2;

public class TypeofRelation {
//
//1>	One to One relationship
//2>	One to many or many to one relationship
//3>	Many to many relationships
//	
//	One to One Relationship: It is used to create a relationship between two tables
//	in which a single row of the first table can only be related to
//	one and only one records of a second table. Similarly, the row of a second 
//	table can also be related to anyone row of the first table.
//	
//	
//	One to Many Relationship: It is used to create a relationship between two tables.
//	Any single rows of the first table can be related to one or more rows of the second 
//	tables, but the rows of second tables can only relate to the only row in the first table. 
//	It is also known as a many to one relationship.
//	
//	
//	Many to Many Relationship: It is many to many relationships that create a relationship between
//	two tables. Each record of the first table can relate to any records (or no records) in the second table.
//	. Similarly, each record of the second table can also relate to more than one record of the first table.
//	It is also represented an N:N relationship.
	
}
